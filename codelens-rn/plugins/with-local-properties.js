/**
 * Expo config plugin: writes android/local.properties during prebuild.
 *
 * Resolves the Android SDK path from (in priority order):
 *   1. ANDROID_HOME env var
 *   2. ANDROID_SDK_ROOT env var (deprecated but still works)
 *   3. Common default: %LOCALAPPDATA%/Android/Sdk (Windows)
 *
 * This ensures `npx expo prebuild --clean` never leaves a broken build
 * because the file was wiped.
 */
const { withDangerousMod } = require("expo/config-plugins");
const fs = require("fs");
const path = require("path");

function resolveAndroidSdk() {
  if (process.env.ANDROID_HOME) return process.env.ANDROID_HOME;
  if (process.env.ANDROID_SDK_ROOT) return process.env.ANDROID_SDK_ROOT;

  // Windows default
  const localAppData = process.env.LOCALAPPDATA;
  if (localAppData) {
    const candidate = path.join(localAppData, "Android", "Sdk");
    if (fs.existsSync(candidate)) return candidate;
  }

  // macOS / Linux defaults
  const home = process.env.HOME || process.env.USERPROFILE;
  if (home) {
    for (const rel of ["Library/Android/sdk", "Android/Sdk"]) {
      const candidate = path.join(home, rel);
      if (fs.existsSync(candidate)) return candidate;
    }
  }

  return null;
}

module.exports = function withLocalProperties(config) {
  return withDangerousMod(config, [
    "android",
    (cfg) => {
      const sdkDir = resolveAndroidSdk();
      if (!sdkDir) {
        console.warn(
          "[with-local-properties] Could not resolve Android SDK path. " +
            "Set ANDROID_HOME or install the SDK to the default location."
        );
        return cfg;
      }

      const androidDir = path.join(cfg.modRequest.platformProjectRoot);
      fs.mkdirSync(androidDir, { recursive: true });

      const escaped = sdkDir.replace(/\\/g, "\\\\");
      const content = [
        "# Auto-generated by plugins/with-local-properties.js",
        "# Do not commit — regenerated on every `expo prebuild`.",
        `sdk.dir=${escaped}`,
        "",
      ].join("\n");

      fs.writeFileSync(path.join(androidDir, "local.properties"), content);
      console.log(`[with-local-properties] Wrote sdk.dir=${sdkDir}`);
      return cfg;
    },
  ]);
};
